package cl.com.ccla.type.datosturismo;


public class Persona {
    
    private String rut;
    private String url;
    
    public Persona() {
        super();
    }

    public void setRut(String rut) {
        this.rut = rut;
    }

    public String getRut() {
        return rut;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
